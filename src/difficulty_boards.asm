load_board_difficulty:


    pusha


    cmp byte [pointer_flag], 1
    JE .easy
    cmp byte [pointer_flag], 2
    JE .medium
    cmp byte [pointer_flag], 3
    JE .hard

    .easy:

        mov si, grid
        mov di, grid_easy
        mov bp, solution
        mov bx, solution_easy
        mov dx, 162
        mov cx, 0

        jmp .load_the_board

    .medium:

        mov si, grid
        mov di, grid_medium
        mov bp, solution
        mov bx, solution_medium
        mov dx, 162
        mov cx, 0

        jmp .load_the_board

    .hard:

        mov si, grid
        mov di, grid_hard
        mov bp, solution
        mov bx, solution_hard
        mov dx, 162
        mov cx, 0

        .load_the_board:
            mov ax, word [di]
            mov word [si], ax
            mov ax, word [bx]
            mov word [bp], ax
            add si, 2
            add di, 2
            add bp, 2
            add bx, 2
            add cx, 2
            cmp cx, dx
            jnz .load_the_board



    popa
    ret